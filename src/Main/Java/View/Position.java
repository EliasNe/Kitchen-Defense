package View;

// TODO This class might be the same as the built in class Point2D.Double, should we use that instead?
public class Position {
    private double x;
    private double y;
    public Position(double x, double y){
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }
    public double getY() {
        return y;
    }

}
